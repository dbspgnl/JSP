DROP TABLE MEMBER;

CREATE TABLE MEMBER (
	ID		VARCHAR2(20)		PRIMARY KEY,
	PW		VARCHAR2(20)		NULL,
	NAME	VARCHAR2(20)		NULL,
	EMAIL	VARCHAR2(30)		NULL,
	ZONECODE	VARCHAR2(10)		NULL,
	ADDR	VARCHAR2(100)		NULL,
	ADDRETC	VARCHAR2(100)		NULL,
	PHONE	VARCHAR2(13)		NULL,
	GRADE	NUMBER(3)		NULL,
	REGDATE	DATE		NULL,
	DROPOUT	VARCHAR2(2)		NULL
);

